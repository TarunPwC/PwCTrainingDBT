
  
    

create or replace table EMART_OMS.L3_CONSUMPTION.CUSTOMERREVENUE
    

    
    as (SELECT
    OS.CUSTOMERID,
    C.CUSTOMERNAME,
    SUM(OS.ORDERCOUNT) AS ORDERCOUNT,
    SUM(OS.REVENUE) AS REVENUE
FROM
    EMART_OMS.L2_PROCESSING.ORDER_FACT OS
JOIN
    EMART_OMS.L2_PROCESSING.CUSTOMER_STG C
ON OS.CUSTOMERID = C.CUSTOMERID
GROUP BY
    OS.CUSTOMERID,
    C.CUSTOMERNAME
    )
;


  